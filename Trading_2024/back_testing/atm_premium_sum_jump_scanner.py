def ist_tz() -> Any:
    """Return an Asia/Kolkata/Calcutta timezone object usable by pandas."""
    if ZoneInfo is not None:
        return ZoneInfo("Asia/Kolkata")
    if pytz is not None:
        return pytz.timezone("Asia/Calcutta")
    # Last-resort string accepted by pandas in most environments.
    return "Asia/Kolkata"


INDIA_TZ = ist_tz()


def _safe_fname_part(value: Any) -> str:
    """Return a filesystem-safe text fragment."""
    s = str(value)
    return "".join(ch if ch.isalnum() or ch in ("-", "_") else "_" for ch in s)


