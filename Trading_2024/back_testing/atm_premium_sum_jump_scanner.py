# Date patterns accepted in filenames.
# The first pattern accepts 2026-06-01, 2026_06_01, and 20260601.
# The second accepts 01-06-2026 and 01_06_2026.
_FILENAME_DATE_PATTERNS = [
    re.compile(r"(?<!\d)(20\d{2})[-_]?([01]\d)[-_]?([0-3]\d)(?!\d)"),
    re.compile(r"(?<!\d)([0-3]\d)[-_]([01]\d)[-_](20\d{2})(?!\d)"),
]

# Matches strings ending with timezone suffix: Z, +0530, +05:30, -0400, etc.
_TZ_SUFFIX_RE = re.compile(r"(?:Z|[+-]\d{2}:?\d{2})$", re.IGNORECASE)

# Detects whether a text contains a full date.
_DATE_IN_TEXT_RE = re.compile(
    r"(?:\d{4}[-/]\d{1,2}[-/]\d{1,2})|(?:\d{1,2}[-/]\d{1,2}[-/]\d{4})"
)

# Detects HH:MM or HH:MM:SS.
_TIME_IN_TEXT_RE = re.compile(r"^\s*\d{1,2}:\d{2}(:\d{2})?(\.\d+)?\s*$")


def extract_date_from_filename(path: Path) -> Optional[date]:
    """Extract a valid trading date from a CSV filename."""
    name = path.name
    for i, pat in enumerate(_FILENAME_DATE_PATTERNS):
        m = pat.search(name)
        if not m:
            continue
        try:
            if i == 0:
                yyyy, mm, dd = int(m.group(1)), int(m.group(2)), int(m.group(3))
            else:
                dd, mm, yyyy = int(m.group(1)), int(m.group(2)), int(m.group(3))
            return date(yyyy, mm, dd)
        except ValueError:
            return None
    return None


def parse_timestamp_series_to_ist(s: pd.Series, file_day: Optional[date]) -> pd.Series:
    """
    Convert a timestamp Series to timezone-aware IST.

    Handles:
      - timezone-aware strings, e.g. 2026-06-01 09:15:02+0530
      - naive datetime strings, e.g. 2026-06-01 09:15:02
      - time-only strings, e.g. 09:15:02, by attaching file_day
    """
    s_str = s.astype("string").str.strip()

    # Convert pure time strings to full datetime strings using the filename date.
    if file_day is not None and ALLOW_TIME_ONLY_TIMESTAMPS:
        is_time_only = s_str.str.match(_TIME_IN_TEXT_RE, na=False)
        date_prefix = file_day.strftime("%Y-%m-%d")
        s_str = s_str.where(~is_time_only, date_prefix + " " + s_str)

    has_tz = s_str.str.contains(_TZ_SUFFIX_RE, regex=True, na=False)

    # Rows with timezone suffix are parsed as aware and then converted to IST.
    aware = pd.to_datetime(s_str.where(has_tz), errors="coerce", utc=True).dt.tz_convert(INDIA_TZ)

    # Rows without timezone suffix are treated as already-IST naive timestamps.
    naive = pd.to_datetime(s_str.where(~has_tz), errors="coerce")
    naive = naive.dt.tz_localize(INDIA_TZ, nonexistent="shift_forward", ambiguous="NaT")

    return aware.fillna(naive)


