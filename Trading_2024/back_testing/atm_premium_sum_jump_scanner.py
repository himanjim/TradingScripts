def list_csv_files(csv_dir: str) -> List[Path]:
    """Return sorted CSV files from the configured folder."""
    folder = Path(csv_dir)
    if not folder.exists():
        raise FileNotFoundError(f"CSV_DIR does not exist: {folder}")
    return sorted(p for p in folder.glob("*.csv") if p.is_file())


def main() -> None:
    """Run full folder analysis."""
    csv_paths = list_csv_files(CSV_DIR)
    if not csv_paths:
        raise FileNotFoundError(f"No CSV files found in: {CSV_DIR}")

    print(f"[INFO] CSV files found: {len(csv_paths)}")
    print(f"[INFO] CSV_DIR: {CSV_DIR}")
    print(f"[INFO] Output: {OUTPUT_XLSX}")
    print(
        "[INFO] Jump rule: "
        f"current minute >= avg(previous {BASELINE_LOOKBACK_MINUTES} valid minutes) "
        f"+ {JUMP_THRESHOLD_PCT:.2f}% and abs jump >= {MIN_ABS_JUMP_RUPEES:.2f}"
    )

    all_minute_scans: List[pd.DataFrame] = []
    all_events: List[pd.DataFrame] = []
    daily_rows: List[Dict[str, Any]] = []
    skipped_rows: List[Dict[str, Any]] = []

    for path in csv_paths:
        file_day = extract_date_from_filename(path)
        if REQUIRE_DATE_IN_FILENAME and file_day is None:
            skipped_rows.append(
                {
                    "source_file": path.name,
                    "day": None,
                    "reason": "Skipped: no valid date in filename",
                }
            )
            print(f"[SKIP] {path.name}: no valid date in filename")
            continue

        try:
            result = process_one_csv(path)
            all_minute_scans.append(result.minute_scan)
            if not result.jump_events.empty:
                all_events.append(result.jump_events)
            daily_rows.append(result.daily_summary)
            skipped_rows.extend(result.skipped_rows)
            print(
                f"[OK] {path.name}: valid_minutes={result.daily_summary['valid_minute_points']} "
                f"jump_events={result.daily_summary['jump_events']} "
                f"largest_jump_pct={result.daily_summary['largest_jump_pct']:.2f}"
            )
        except Exception as exc:
            skipped_rows.append(
                {
                    "source_file": path.name,
                    "day": file_day,
                    "reason": f"Skipped: {exc}",
                }
            )
            msg = f"[WARN] {path.name}: {exc}"
            if FAIL_ON_CSV_ERROR:
                raise RuntimeError(msg) from exc
            print(msg)

    minute_scan_df = pd.concat(all_minute_scans, ignore_index=True) if all_minute_scans else pd.DataFrame()
    jump_events_df = pd.concat(all_events, ignore_index=True) if all_events else pd.DataFrame()
    daily_df = pd.DataFrame(daily_rows)
    skipped_df = pd.DataFrame(skipped_rows)

    if not daily_df.empty:
        daily_df = daily_df.sort_values(["day", "source_file"]).reset_index(drop=True)
    if not jump_events_df.empty:
        jump_events_df = jump_events_df.sort_values(["day", "source_file", "event_id"]).reset_index(drop=True)
    if not minute_scan_df.empty:
        minute_scan_df = minute_scan_df.sort_values(["day", "source_file", "minute_ist"]).reset_index(drop=True)

    overall_df = build_overall_summary(daily_df, jump_events_df)
    monthwise_df = build_monthwise_summary(daily_df, jump_events_df)

    write_excel(
        jump_events_df=jump_events_df,
        daily_df=daily_df,
        overall_df=overall_df,
        monthwise_df=monthwise_df,
        minute_scan_df=minute_scan_df,
        skipped_df=skipped_df,
    )

    if daily_df.empty:
        print("[WARN] No valid daily CSVs processed. Check skipped_files sheet.")
    else:
        print("\n[SUMMARY]")
        print(f"Processed days/files: {len(daily_df)}")
        print(f"Total jump events: {int(daily_df['jump_events'].sum())}")
        print(f"Days with at least one jump: {int((daily_df['jump_events'] > 0).sum())}")
        if not jump_events_df.empty:
            print(f"Average peak jump pct: {jump_events_df['peak_jump_pct'].mean():.2f}%")
            print(f"Maximum peak jump pct: {jump_events_df['peak_jump_pct'].max():.2f}%")
            print(f"Average peak jump rupees: {jump_events_df['peak_jump_abs'].mean():.2f}")
            print(f"Maximum peak jump rupees: {jump_events_df['peak_jump_abs'].max():.2f}")


if __name__ == "__main__":
    main()
