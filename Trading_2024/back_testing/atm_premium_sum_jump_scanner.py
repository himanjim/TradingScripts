def process_one_csv(path: Path) -> FileResult:
    """Process one CSV file and return minute scan, jump events, and summary."""
    file_day = extract_date_from_filename(path)
    skipped_rows: List[Dict[str, Any]] = []

    if REQUIRE_DATE_IN_FILENAME and file_day is None:
        raise ValueError("No valid trading date found in filename")

    read_res = load_and_normalize_csv(path, file_day=file_day)
    raw = read_res.raw_df.copy()
    raw_rows = int(raw.shape[0])

    invalid_ts = int(raw["timestamp"].isna().sum())
    invalid_premium = int(raw["premium"].isna().sum())
    if invalid_ts or invalid_premium:
        skipped_rows.append(
            {
                "source_file": path.name,
                "day": file_day,
                "reason": "Invalid timestamp or premium rows removed",
                "invalid_timestamp_rows": invalid_ts,
                "invalid_premium_rows": invalid_premium,
            }
        )

    raw = raw.dropna(subset=["timestamp", "premium"]).copy()
    if raw.empty:
        raise ValueError("No valid timestamp/premium rows after parsing")

    raw["local_day"] = raw["timestamp"].dt.date

    # If filename had no date and that was allowed, use the single row date.
    if file_day is None:
        unique_days = sorted(raw["local_day"].dropna().unique().tolist())
        if len(unique_days) != 1:
            raise ValueError("Filename has no date and CSV contains zero/multiple dates")
        file_day = unique_days[0]

    rows_before_date_filter = int(raw.shape[0])
    if ENFORCE_FILENAME_DATE:
        raw = raw[raw["local_day"] == file_day].copy()
    rows_after_date_filter = int(raw.shape[0])

    if rows_after_date_filter == 0:
        raise ValueError("No rows match the date extracted from filename")

    if rows_before_date_filter != rows_after_date_filter:
        skipped_rows.append(
            {
                "source_file": path.name,
                "day": file_day,
                "reason": "Rows with date different from filename date removed",
                "removed_rows": rows_before_date_filter - rows_after_date_filter,
            }
        )

    raw["local_time"] = raw["timestamp"].dt.time
    raw = raw[(raw["local_time"] >= SESSION_START_IST) & (raw["local_time"] <= SESSION_END_IST)].copy()
    rows_after_session_filter = int(raw.shape[0])

    if raw.empty:
        raise ValueError("No valid rows inside configured market session")

    # Remove exact duplicate timestamps. For live appends, keep the last value.
    duplicate_ts = int(raw.duplicated(subset=["timestamp"], keep="last").sum())
    if duplicate_ts:
        skipped_rows.append(
            {
                "source_file": path.name,
                "day": file_day,
                "reason": "Duplicate timestamps removed; last row retained",
                "duplicate_timestamp_rows": duplicate_ts,
            }
        )
    raw = raw.drop_duplicates(subset=["timestamp"], keep="last").sort_values("timestamp")

    minute_df = aggregate_to_minutes(raw, file_day)
    minute_df = add_jump_columns(minute_df)

    valid_minute_points = int(minute_df["premium"].notna().sum())
    if valid_minute_points < MIN_VALID_MINUTES_PER_DAY:
        raise ValueError(
            f"Too few valid minute points: {valid_minute_points}; "
            f"minimum required is {MIN_VALID_MINUTES_PER_DAY}"
        )

    events_df = build_jump_events(minute_df, source_file=path.name, day_d=file_day)

    # Add identity columns to minute scan after calculations.
    minute_df = minute_df.reset_index()
    minute_df.insert(0, "day", file_day)
    minute_df.insert(1, "instrument_label", INSTRUMENT_LABEL)
    minute_df.insert(2, "source_file", path.name)
    minute_df["minute"] = pd.to_datetime(minute_df["minute"]).dt.tz_convert(INDIA_TZ)
    minute_df["minute_ist"] = minute_df["minute"].dt.strftime("%Y-%m-%d %H:%M:%S%z")

    # Keep a practical order for the most important minute-level columns.
    first_cols = [
        "day",
        "instrument_label",
        "source_file",
        "minute_ist",
        "time",
        "premium",
        "previous_premium",
        "baseline_avg",
        "baseline_valid_minutes",
        "jump_abs",
        "jump_pct",
        "jump_vs_prev_abs",
        "jump_vs_prev_pct",
        "jump_flag",
    ]
    rest_cols = [c for c in minute_df.columns if c not in first_cols and c != "minute"]
    minute_df = minute_df[first_cols + rest_cols]

    summary = summarize_day(
        source_file=path.name,
        day_d=file_day,
        raw_rows=raw_rows,
        rows_after_date_filter=rows_after_date_filter,
        rows_after_session_filter=rows_after_session_filter,
        minute_df=add_jump_columns(aggregate_to_minutes(raw, file_day)),
        events_df=events_df,
        timestamp_col=read_res.timestamp_col,
        value_col=read_res.value_col,
        assumed_headerless=read_res.assumed_headerless,
    )

    return FileResult(
        minute_scan=minute_df,
        jump_events=events_df,
        daily_summary=summary,
        skipped_rows=skipped_rows,
    )


