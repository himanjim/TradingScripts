r"""
premium_sum_csv_jump_analyzer.py

Offline analyzer for premium-sum CSV files generated by OptionsPremiumTracker.py.

Use case
--------
Your live tracker writes rows like:

    timestamp,value
    2026-06-01 09:15:02+0530,30250.0
    2026-06-01 09:15:04+0530,30275.0

where "value" is the ATM CE+PE premium sum multiplied by lots.
This script reads those daily CSV files from a folder and finds sudden premium
jumps, for example 30000 -> 33000 = 10% jump.

Main analysis logic
-------------------
1. Only CSV files with a proper trading date in the filename are processed.
   Example accepted names:
       premium_data_2026-06-01.csv
       premium_data_20260601.csv
       premium_data_01-06-2026.csv

2. Each CSV is normalized to one row per minute. The default minute value is the
   last observed premium in that minute, because this is closest to a tradeable
   decision point. You may change MINUTE_AGG_METHOD to mean/max/min/median.

3. For every minute, the script compares current premium against the average of
   the immediately preceding BASELINE_LOOKBACK_MINUTES valid minutes.

   Example with BASELINE_LOOKBACK_MINUTES=3:
       10:04 premium is compared with average of 10:01, 10:02, 10:03.

4. A jump is flagged when:
       jump_pct >= JUMP_THRESHOLD_PCT
       and jump_abs >= MIN_ABS_JUMP_RUPEES

5. Consecutive jump minutes are grouped into one jump event so that a single
   spike does not create many duplicate event rows.

6. Excel output contains:
       - jump_events: one row per jump episode
       - daily_summary: one row per CSV/day
       - overall_summary: aggregate metrics across all days
       - monthwise_summary: monthly jump statistics
       - minute_scan: optional minute-level data with jump calculations
       - skipped_files: files/rows skipped and why
       - config: exact parameters used

Windows CMD examples
--------------------
Default folder is:
    %USERPROFILE%\Downloads\PremiumsChartsData

Run:
    python premium_sum_csv_jump_analyzer.py

Run with custom folder and 10% jump threshold:
    set CSV_DIR=G:\My Drive\Trading\PremiumsChartsData
    set JUMP_THRESHOLD_PCT=10
    set BASELINE_LOOKBACK_MINUTES=3
    set MIN_VALID_BASELINE_MINUTES=2
    python premium_sum_csv_jump_analyzer.py

Run with larger minimum rupee jump filter:
    set MIN_ABS_JUMP_RUPEES=1000
    python premium_sum_csv_jump_analyzer.py

Dependencies
------------
    pip install pandas openpyxl

This script does not use Kite, pickles, OptionTradeUtils, or live market data.
It only analyzes already-generated CSV files.
"""

