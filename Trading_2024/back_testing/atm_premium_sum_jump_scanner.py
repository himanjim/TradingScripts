def build_monthwise_summary(daily_df: pd.DataFrame, events_df: pd.DataFrame) -> pd.DataFrame:
    """Build monthly jump summary."""
    if daily_df.empty:
        return pd.DataFrame()

    base = (
        daily_df.groupby("month", as_index=False)
        .agg(
            processed_days=("day", "nunique"),
            days_with_jump=("jump_events", lambda s: int((s > 0).sum())),
            total_jump_events=("jump_events", "sum"),
            avg_jump_events_per_day=("jump_events", "mean"),
            median_jump_events_per_day=("jump_events", "median"),
            avg_daily_avg_premium=("avg_premium", "mean"),
            avg_daily_max_premium=("max_premium", "mean"),
            max_daily_largest_jump_pct=("largest_jump_pct", "max"),
            max_daily_largest_jump_abs=("largest_jump_abs", "max"),
        )
        .sort_values("month")
        .reset_index(drop=True)
    )
    base["days_with_jump_pct"] = 100.0 * base["days_with_jump"] / base["processed_days"]

    if not events_df.empty:
        e = events_df.copy()
        e["month"] = pd.to_datetime(e["day"]).dt.strftime("%Y-%m")
        event_stats = (
            e.groupby("month", as_index=False)
            .agg(
                avg_event_peak_jump_pct=("peak_jump_pct", "mean"),
                median_event_peak_jump_pct=("peak_jump_pct", "median"),
                max_event_peak_jump_pct=("peak_jump_pct", "max"),
                avg_event_peak_jump_abs=("peak_jump_abs", "mean"),
                max_event_peak_jump_abs=("peak_jump_abs", "max"),
                avg_event_duration_minutes=("duration_minutes", "mean"),
                max_event_duration_minutes=("duration_minutes", "max"),
            )
            .reset_index(drop=True)
        )
        base = base.merge(event_stats, on="month", how="left")

    return base


