def read_csv_flex(path: Path) -> pd.DataFrame:
    """Read a CSV with common encodings."""
    last_err: Optional[Exception] = None
    for enc in ("utf-8-sig", "utf-8", "cp1252", "latin1"):
        try:
            return pd.read_csv(path, encoding=enc)
        except Exception as exc:
            last_err = exc
    if last_err is not None:
        raise last_err
    raise RuntimeError(f"Could not read CSV: {path}")


def read_csv_headerless(path: Path) -> pd.DataFrame:
    """Read a CSV as headerless two-column data."""
    last_err: Optional[Exception] = None
    for enc in ("utf-8-sig", "utf-8", "cp1252", "latin1"):
        try:
            return pd.read_csv(path, encoding=enc, header=None)
        except Exception as exc:
            last_err = exc
    if last_err is not None:
        raise last_err
    raise RuntimeError(f"Could not read headerless CSV: {path}")


def choose_timestamp_and_value_columns(df: pd.DataFrame) -> Optional[Tuple[str, str]]:
    """
    Choose timestamp and premium-value columns from a possibly renamed CSV.

    Preferred schema from OptionsPremiumTracker.py is timestamp,value.
    Fallback: first datetime-like column + first mostly numeric column.
    """
    if df.empty or len(df.columns) < 2:
        return None

    norm_to_original: Dict[str, str] = {normalize_col_name(c): c for c in df.columns}

    timestamp_col: Optional[str] = None
    value_col: Optional[str] = None

    for cand in TIMESTAMP_COL_CANDIDATES:
        if cand in norm_to_original:
            timestamp_col = norm_to_original[cand]
            break

    for cand in VALUE_COL_CANDIDATES:
        if cand in norm_to_original:
            value_col = norm_to_original[cand]
            break

    if timestamp_col and value_col and timestamp_col != value_col:
        return timestamp_col, value_col

    # Fallback: infer timestamp column by parse success ratio.
    best_ts_col: Optional[str] = None
    best_ts_score = -1.0
    for col in df.columns:
        sample = df[col].dropna().astype("string").head(200)
        if sample.empty:
            continue
        # Basic prefilter prevents a numeric premium column from being parsed as
        # weird epoch-like dates.
        text_has_date_or_time = sample.str.contains(_DATE_IN_TEXT_RE, regex=True, na=False) | sample.str.contains(
            r"\d{1,2}:\d{2}", regex=True, na=False
        )
        if text_has_date_or_time.mean() < 0.5:
            continue
        parsed = pd.to_datetime(sample, errors="coerce")
        score = float(parsed.notna().mean())
        if score > best_ts_score:
            best_ts_score = score
            best_ts_col = col

    timestamp_col = timestamp_col or best_ts_col

    # Fallback: infer premium column by numeric conversion ratio.
    best_val_col: Optional[str] = None
    best_val_score = -1.0
    for col in df.columns:
        if col == timestamp_col:
            continue
        sample = df[col].dropna().astype("string").head(500)
        if sample.empty:
            continue
        cleaned = sample.str.replace(",", "", regex=False).str.replace("₹", "", regex=False).str.strip()
        numeric = pd.to_numeric(cleaned, errors="coerce")
        score = float(numeric.notna().mean())
        if score > best_val_score:
            best_val_score = score
            best_val_col = col

    value_col = value_col or best_val_col

    if timestamp_col is None or value_col is None or timestamp_col == value_col:
        return None
    return timestamp_col, value_col


def load_and_normalize_csv(path: Path, file_day: Optional[date]) -> ReadResult:
    """Read one CSV and return a normalized timestamp/value DataFrame."""
    df = read_csv_flex(path)
    chosen = choose_timestamp_and_value_columns(df)
    assumed_headerless = False

    # If normal read did not detect the two columns, retry as headerless.
    if chosen is None:
        df2 = read_csv_headerless(path)
        if df2.shape[1] < 2:
            raise ValueError("CSV does not contain at least two columns")
        df2 = df2.iloc[:, :2].copy()
        df2.columns = ["timestamp", "value"]
        df = df2
        chosen = ("timestamp", "value")
        assumed_headerless = True

    ts_col, val_col = chosen
    out = df[[ts_col, val_col]].copy()
    out.columns = ["timestamp_raw", "premium_raw"]

    out["timestamp"] = parse_timestamp_series_to_ist(out["timestamp_raw"], file_day=file_day)

    # Clean rupee symbols and thousands separators before numeric conversion.
    premium_text = out["premium_raw"].astype("string")
    premium_text = premium_text.str.replace(",", "", regex=False).str.replace("₹", "", regex=False).str.strip()
    out["premium"] = pd.to_numeric(premium_text, errors="coerce")

    return ReadResult(raw_df=out, timestamp_col=str(ts_col), value_col=str(val_col), assumed_headerless=assumed_headerless)


