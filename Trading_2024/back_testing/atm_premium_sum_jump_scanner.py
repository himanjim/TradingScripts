def full_session_index(day_d: date) -> pd.DatetimeIndex:
    """Create complete 1-minute IST index for the configured session."""
    start = pd.Timestamp(datetime.combine(day_d, SESSION_START_IST), tz=INDIA_TZ)
    end = pd.Timestamp(datetime.combine(day_d, SESSION_END_IST), tz=INDIA_TZ)
    return pd.date_range(start=start, end=end, freq="1min")


def aggregate_to_minutes(raw: pd.DataFrame, day_d: date) -> pd.DataFrame:
    """
    Convert raw tick/second-level premium rows into one row per minute.

    The returned DataFrame is reindexed to the full session. Missing minutes are
    kept as NaN so that the rolling baseline does not silently invent data.
    """
    if MINUTE_AGG_METHOD not in {"last", "mean", "median", "max", "min", "first"}:
        raise ValueError(
            "MINUTE_AGG_METHOD must be one of: last, mean, median, max, min, first. "
            f"Got: {MINUTE_AGG_METHOD}"
        )

    tmp = raw.dropna(subset=["timestamp", "premium"]).copy()
    tmp = tmp.sort_values("timestamp")
    tmp["minute"] = tmp["timestamp"].dt.floor("min")

    grouped = tmp.groupby("minute", as_index=True)
    if MINUTE_AGG_METHOD == "last":
        premium = grouped["premium"].last()
    elif MINUTE_AGG_METHOD == "first":
        premium = grouped["premium"].first()
    elif MINUTE_AGG_METHOD == "mean":
        premium = grouped["premium"].mean()
    elif MINUTE_AGG_METHOD == "median":
        premium = grouped["premium"].median()
    elif MINUTE_AGG_METHOD == "max":
        premium = grouped["premium"].max()
    elif MINUTE_AGG_METHOD == "min":
        premium = grouped["premium"].min()
    else:  # pragma: no cover
        raise AssertionError("unreachable")

    agg = pd.DataFrame({"premium": premium})
    agg["raw_points_in_minute"] = grouped["premium"].size()
    agg["premium_first_in_minute"] = grouped["premium"].first()
    agg["premium_last_in_minute"] = grouped["premium"].last()
    agg["premium_min_in_minute"] = grouped["premium"].min()
    agg["premium_max_in_minute"] = grouped["premium"].max()

    idx = full_session_index(day_d)
    minute_df = pd.DataFrame(index=idx)
    minute_df.index.name = "minute"
    minute_df = minute_df.join(agg, how="left")
    return minute_df


def add_jump_columns(minute_df: pd.DataFrame) -> pd.DataFrame:
    """Add rolling-baseline and jump columns to a one-minute premium DataFrame."""
    out = minute_df.copy()
    premium = pd.to_numeric(out["premium"], errors="coerce")

    previous = premium.shift(1)
    baseline_source = premium.shift(1)
    rolling = baseline_source.rolling(
        window=BASELINE_LOOKBACK_MINUTES,
        min_periods=MIN_VALID_BASELINE_MINUTES,
    )

    out["previous_premium"] = previous
    out["baseline_avg"] = rolling.mean()
    out["baseline_min"] = rolling.min()
    out["baseline_max"] = rolling.max()
    out["baseline_std"] = rolling.std(ddof=0)
    out["baseline_valid_minutes"] = rolling.count()

    out["jump_abs"] = premium - out["baseline_avg"]
    out["jump_pct"] = (out["jump_abs"] / out["baseline_avg"]) * 100.0
    out["jump_vs_prev_abs"] = premium - out["previous_premium"]
    out["jump_vs_prev_pct"] = (out["jump_vs_prev_abs"] / out["previous_premium"]) * 100.0

    out["drop_abs"] = out["baseline_avg"] - premium
    out["drop_pct"] = (out["drop_abs"] / out["baseline_avg"]) * 100.0

    out["jump_flag"] = (
        premium.notna()
        & out["baseline_avg"].notna()
        & (out["baseline_avg"] > 0)
        & (out["jump_abs"] >= MIN_ABS_JUMP_RUPEES)
        & (out["jump_pct"] >= JUMP_THRESHOLD_PCT)
    )

    out["time"] = out.index.strftime("%H:%M")
    return out


def build_jump_events(minute_df: pd.DataFrame, *, source_file: str, day_d: date) -> pd.DataFrame:
    """Group consecutive jump-flag minutes into jump events."""
    if minute_df.empty or "jump_flag" not in minute_df.columns:
        return pd.DataFrame()

    flags = minute_df["jump_flag"].fillna(False).astype(bool)
    if not flags.any():
        return pd.DataFrame()

    # Consecutive True blocks become event_id 1, 2, 3, ...
    start_of_event = flags & ~flags.shift(1, fill_value=False)
    event_id_series = start_of_event.cumsum().where(flags)

    rows: List[Dict[str, Any]] = []
    session_start_ts = pd.Timestamp(datetime.combine(day_d, SESSION_START_IST), tz=INDIA_TZ)

    for event_id in sorted(event_id_series.dropna().unique()):
        event_mask = event_id_series.eq(event_id)
        event = minute_df.loc[event_mask].copy()
        if event.empty:
            continue

        start_ts = event.index[0]
        end_ts = event.index[-1]
        peak_ts = event["jump_pct"].idxmax()
        peak = minute_df.loc[peak_ts]
        start_row = minute_df.loc[start_ts]

        duration_min = int((end_ts - start_ts).total_seconds() // 60) + 1
        minutes_from_open = int((start_ts - session_start_ts).total_seconds() // 60)

        # Follow-through after the event. This tells whether a jump immediately
        # kept expanding or reverted in the next few minutes.
        lookahead_end_ts = end_ts + pd.Timedelta(minutes=POST_EVENT_LOOKAHEAD_MINUTES)
        future = minute_df.loc[(minute_df.index > end_ts) & (minute_df.index <= lookahead_end_ts)]
        future_valid = future[future["premium"].notna()]

        if future_valid.empty:
            premium_after_n = float("nan")
            max_premium_after_n = float("nan")
            min_premium_after_n = float("nan")
            post_event_change_from_peak_pct = float("nan")
        else:
            premium_after_n = float(future_valid["premium"].iloc[-1])
            max_premium_after_n = float(future_valid["premium"].max())
            min_premium_after_n = float(future_valid["premium"].min())
