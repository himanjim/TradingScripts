def safe_float(value: Any, default: float = 0.0) -> float:
    """Convert value to float without raising on NaN/object errors."""
    try:
        v = float(value)
        if pd.isna(v):
            return default
        return v
    except Exception:
        return default


def summarize_day(
    *,
    source_file: str,
    day_d: date,
    raw_rows: int,
    rows_after_date_filter: int,
    rows_after_session_filter: int,
    minute_df: pd.DataFrame,
    events_df: pd.DataFrame,
    timestamp_col: str,
    value_col: str,
    assumed_headerless: bool,
) -> Dict[str, Any]:
    """Build one summary row for a CSV/trading day."""
    valid_minutes = minute_df[minute_df["premium"].notna()].copy()

    if valid_minutes.empty:
        first_time = None
        last_time = None
        open_premium = close_premium = min_premium = max_premium = avg_premium = float("nan")
        day_range_abs = day_range_pct = float("nan")
    else:
        first_time = valid_minutes.index[0].strftime("%H:%M")
        last_time = valid_minutes.index[-1].strftime("%H:%M")
        open_premium = float(valid_minutes["premium"].iloc[0])
        close_premium = float(valid_minutes["premium"].iloc[-1])
        min_premium = float(valid_minutes["premium"].min())
        max_premium = float(valid_minutes["premium"].max())
        avg_premium = float(valid_minutes["premium"].mean())
        day_range_abs = max_premium - min_premium
        day_range_pct = (day_range_abs / open_premium * 100.0) if open_premium > 0 else float("nan")

    jump_minutes = int(minute_df["jump_flag"].fillna(False).sum()) if "jump_flag" in minute_df.columns else 0
    event_count = int(len(events_df))

    if events_df.empty:
        first_jump_time = None
        largest_jump_time = None
        largest_jump_pct = 0.0
        largest_jump_abs = 0.0
        avg_event_peak_jump_pct = 0.0
        avg_event_peak_jump_abs = 0.0
        median_event_peak_jump_pct = 0.0
        max_event_duration_minutes = 0
        avg_events_per_day_marker = 0.0
    else:
        first_jump_time = str(events_df["event_start_time"].iloc[0])
        peak_idx = events_df["peak_jump_pct"].idxmax()
        largest_jump_time = str(events_df.loc[peak_idx, "event_peak_time"])
        largest_jump_pct = float(events_df["peak_jump_pct"].max())
        largest_jump_abs = float(events_df["peak_jump_abs"].max())
        avg_event_peak_jump_pct = float(events_df["peak_jump_pct"].mean())
        avg_event_peak_jump_abs = float(events_df["peak_jump_abs"].mean())
        median_event_peak_jump_pct = float(events_df["peak_jump_pct"].median())
        max_event_duration_minutes = int(events_df["duration_minutes"].max())
        avg_events_per_day_marker = float(event_count)

    return {
        "day": day_d,
        "month": pd.Timestamp(day_d).strftime("%Y-%m"),
        "instrument_label": INSTRUMENT_LABEL,
        "source_file": source_file,
        "timestamp_col_used": timestamp_col,
        "value_col_used": value_col,
        "assumed_headerless_csv": assumed_headerless,
        "raw_rows": raw_rows,
        "rows_after_date_filter": rows_after_date_filter,
        "rows_after_session_filter": rows_after_session_filter,
        "valid_minute_points": int(valid_minutes.shape[0]),
        "missing_session_minutes": int(minute_df["premium"].isna().sum()),
        "first_premium_time": first_time,
        "last_premium_time": last_time,
        "open_premium": open_premium,
        "close_premium": close_premium,
        "min_premium": min_premium,
        "max_premium": max_premium,
        "avg_premium": avg_premium,
        "day_range_abs": day_range_abs,
        "day_range_pct_of_open": day_range_pct,
        "jump_threshold_pct": JUMP_THRESHOLD_PCT,
        "min_abs_jump_rupees": MIN_ABS_JUMP_RUPEES,
        "jump_events": event_count,
        "jump_minutes": jump_minutes,
        "first_jump_time": first_jump_time,
        "largest_jump_time": largest_jump_time,
        "largest_jump_pct": largest_jump_pct,
        "largest_jump_abs": largest_jump_abs,
        "avg_event_peak_jump_pct": avg_event_peak_jump_pct,
        "median_event_peak_jump_pct": median_event_peak_jump_pct,
        "avg_event_peak_jump_abs": avg_event_peak_jump_abs,
        "max_event_duration_minutes": max_event_duration_minutes,
        "events_per_day": avg_events_per_day_marker,
    }


